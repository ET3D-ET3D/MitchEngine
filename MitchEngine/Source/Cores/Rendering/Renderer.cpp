void Renderer::Render()
{
	Camera* CurrentCamera = Camera::CurrentCamera;
	if (!CurrentCamera)
	{
		return;
	}

	glClearColor(0.1f, 0.1f, 0.1f, 1.0f);
	glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT);

	glBindFramebuffer(GL_FRAMEBUFFER, gBuffer);

	glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT);
	SkyboxShader->Use();
	SkyboxShader->SetInt("skybox", 0);

	// world space positions of our cubes
	glm::vec3 cubePositions[] = {
		glm::vec3(0.0f,  0.0f,  0.0f),
		glm::vec3(2.0f,  5.0f, -15.0f),
		glm::vec3(-1.5f, -2.2f, -2.5f),
		glm::vec3(-3.8f, -2.0f, -12.3f),
		glm::vec3(2.4f, -0.4f, -3.5f),
		glm::vec3(-1.7f,  3.0f, -7.5f),
		glm::vec3(1.3f, -2.0f, -2.5f),
		glm::vec3(1.5f,  2.0f, -2.5f),
		glm::vec3(1.5f,  0.2f, -1.5f),
		glm::vec3(-1.3f,  1.0f, -1.5f)
	};
	float SCR_WIDTH = (float)Window::WINDOW_WIDTH;
	float SCR_HEIGHT = (float)Window::WINDOW_HEIGHT;

	glm::mat4 projection = glm::perspective(glm::radians(Camera::CurrentCamera->Zoom), SCR_WIDTH / SCR_HEIGHT, 0.1f, 100.0f);
	glm::mat4 view = Camera::CurrentCamera->GetViewMatrix();


	int i = 0;
	auto Renderables = GetEntities();
	for (auto& InEntity : Renderables)
	{
		Transform& trans = InEntity.GetComponent<Transform>();
		Model& modelComponent = InEntity.GetComponent<Model>();

		Shader* shader = modelComponent.ModelShader;
		shader->Use();
		GLuint Program = shader->GetProgram();

		shader->SetMat4("projection", projection);

		shader->SetMat4("view", view);

		// calculate the model matrix for each object and pass it to shader before drawing
		//glm::mat4 model(1.0f);
		//model = glm::translate(model, trans.GetPosition());
		//model = glm::scale(model, glm::vec3(1, 1, 1));
		//model = model * glm::toMat4(trans.Rotation);
		//model = glm::rotate(glm::toMat4(trans.Rotation), glm::vec3(1,1,1));
		//model = model * glm::toMat4(trans.Rotation);
		glm::mat4 model = trans.WorldTransform;
		model = glm::scale(model, glm::vec3(1, 1, 1));
		shader->SetMat4("model", model);
		modelComponent.Draw();
		//cube.DrawCube();
		i++;
	}
	glBindFramebuffer(GL_FRAMEBUFFER, 0);

	// 2. lighting pass: calculate lighting by iterating over a screen filled quad pixel-by-pixel using the gbuffer's content.
	// -----------------------------------------------------------------------------------------------------------------------
	glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT);
	LightingPassShader->Use();
	glActiveTexture(GL_TEXTURE0);
	glBindTexture(GL_TEXTURE_2D, gPosition);
	glActiveTexture(GL_TEXTURE1);
	glBindTexture(GL_TEXTURE_2D, gNormal);
	glActiveTexture(GL_TEXTURE2);
	glBindTexture(GL_TEXTURE_2D, gAlbedoSpec);
	// send light relevant uniforms
	for (unsigned int i = 0; i < lightPositions.size(); i++)
	{
		LightingPassShader->SetVec3("lights[" + std::to_string(i) + "].Position", lightPositions[i]);
		LightingPassShader->SetVec3("lights[" + std::to_string(i) + "].Color", lightColors[i]);
		// update attenuation parameters and calculate radius
		const float constant = 1.0; // note that we don't send this to the shader, we assume it is always 1.0 (in our case)
		const float linear = 0.7;
		const float quadratic = 1.8;
		LightingPassShader->SetFloat("lights[" + std::to_string(i) + "].Linear", linear);
		LightingPassShader->SetFloat("lights[" + std::to_string(i) + "].Quadratic", quadratic);
		// then calculate radius of light volume/sphere
		const float maxBrightness = std::fmaxf(std::fmaxf(lightColors[i].r, lightColors[i].g), lightColors[i].b);
		float radius = (-linear + std::sqrt(linear * linear - 4 * quadratic * (constant - (256.0f / 5.0f) * maxBrightness))) / (2.0f * quadratic);
		LightingPassShader->SetFloat("lights[" + std::to_string(i) + "].Radius", radius);
	}
	LightingPassShader->SetVec3("viewPos", Camera::CurrentCamera->Position);

	renderQuad();

	// 2.5. copy content of geometry's depth buffer to default framebuffer's depth buffer
	// ----------------------------------------------------------------------------------
	glBindFramebuffer(GL_READ_FRAMEBUFFER, gBuffer);
	glBindFramebuffer(GL_DRAW_FRAMEBUFFER, 0); // write to default framebuffer
											   // blit to default framebuffer. Note that this may or may not work as the internal formats of both the FBO and default framebuffer have to match.
											   // the internal formats are implementation defined. This works on all of my systems, but if it doesn't on yours you'll likely have to write to the 		
											   // depth buffer in another shader stage (or somehow see to match the default framebuffer's internal format with the FBO's internal format).
	glBlitFramebuffer(0, 0, SCR_WIDTH, SCR_HEIGHT, 0, 0, SCR_WIDTH, SCR_HEIGHT, GL_DEPTH_BUFFER_BIT, GL_NEAREST);
	glBindFramebuffer(GL_FRAMEBUFFER, 0);

	LightingBoxShader->Use();
	LightingBoxShader->SetMat4("projection", projection);
	LightingBoxShader->SetMat4("view", view);
	for (unsigned int i = 0; i < lightPositions.size(); i++)
	{
		glm::mat4 model = glm::mat4(1.f);
		model = glm::translate(model, lightPositions[i]);
		model = glm::scale(model, glm::vec3(0.125f));
		LightingBoxShader->SetMat4("model", model);
		LightingBoxShader->SetVec3("lightColor", lightColors[i]);
		//renderCube();
	}

	// draw skybox as last
	glDepthFunc(GL_LEQUAL);  // change depth function so depth test passes when values are equal to depth buffer's content
	SkyboxShader->Use();
	SkyboxShader->SetMat4("view", glm::mat4(glm::mat3(Camera::CurrentCamera->GetViewMatrix())));
	SkyboxShader->SetMat4("projection", projection);
	// skybox cube
	glBindVertexArray(skyboxVAO);
	glActiveTexture(GL_TEXTURE0);
	glBindTexture(GL_TEXTURE_CUBE_MAP, SkyboxMap->Id);
	glDrawArrays(GL_TRIANGLES, 0, 36);
	glBindVertexArray(0);
	glDepthFunc(GL_LESS); // set depth function back to default
}